df = pd.DataFrame(data, columns=[
    "Компания", "Год", "Выручка", "Себестоимость", "Активы", "Персонал"
])

# -----------------------------
# 2. Корреляция абсолютных значений за 2024 год
# -----------------------------
df_2024 = df[df["Год"] == 2024].copy()

abs_cols = ["Выручка", "Себестоимость", "Активы", "Персонал"]
corr_abs = df_2024[abs_cols].corr(method="pearson")

plt.figure(figsize=(8, 6))
sns.heatmap(corr_abs, annot=True, cmap="inferno", fmt=".2f", linewidths=0.5)
plt.title("Корреляционная матрица абсолютных значений, 2024")
plt.tight_layout()
plt.show()

print("Пары показателей с сильной связью в абсолютных значениях (|r| > 0.7):")
for a, b in combinations(abs_cols, 2):
    r = corr_abs.loc[a, b]
    if abs(r) > 0.7:
        print(f"{a} — {b}: r = {r:.2f}")

# -----------------------------
# 3. Построение индексов по каждой компании
#    Базисный год = 2021
# -----------------------------
df_sorted = df.sort_values(["Компания", "Год"]).copy()

for col in abs_cols:
    df_sorted[f"Индекс_{col}"] = df_sorted.groupby("Компания")[col].transform(lambda x: x / x.iloc[0])

index_cols = [f"Индекс_{col}" for col in abs_cols]

# для анализа индексов берём 2022-2024, чтобы не было сплошных единиц за 2021
df_index = df_sorted[df_sorted["Год"] > 2021].copy()

corr_idx = df_index[index_cols].corr(method="pearson")

plt.figure(figsize=(8, 6))
sns.heatmap(corr_idx, annot=True, cmap="viridis", fmt=".2f", linewidths=0.5)
plt.title("Корреляционная матрица индексов, 2022–2024")
plt.tight_layout()
plt.show()

print("\nПары индексов с сильной связью (|r| > 0.7):")
for a, b in combinations(index_cols, 2):
    r = corr_idx.loc[a, b]
    if abs(r) > 0.7:
        print(f"{a} — {b}: r = {r:.2f}")

